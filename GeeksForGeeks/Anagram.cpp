class Solution
{
    public:
    //Function is to check whether two strings are anagram of each other or not.
    bool isAnagram(string a, string b){
        int chars[26];
        memset(chars, 0, sizeof(chars));
        for(auto val: a)
            chars[val - 'a']++;
        for(auto val: b)
            chars[val - 'a']--;
        for(auto val: chars){
            if(val != 0){
                return false;
            }
        }
        return true;
    }

};

