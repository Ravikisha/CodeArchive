class Solution{
  public:
    int missingNumber(vector<int>& array, int n) {
        int x1 = array[0];
        for(int i=1;i < n-1;i++){
            x1 ^= array[i];
        }
        int x2 = 1;
        for(int i=2;i<=n;i++){
            x2 ^= i;
        }
        return x1 ^ x2;
    }
};

//{ Driver Code Starts.

int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;

        vector<int> array(n - 1);
        for (int i = 0; i < n - 1; ++i) cin >> array[i];
        Solution obj;
        cout << obj.missingNumber(array, n) << "\n";
    }
    return 0;
}
